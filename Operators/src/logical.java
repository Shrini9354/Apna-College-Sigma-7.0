public class logical {
    public static void main(String[] args) {
        System.out.println((3<0) && (5>0));//false because one condition is false 
        System.out.println((3<0) || (5>0));//if one condition is true then it return true 
        System.out.println(!(3>2));//it becomes true condition into false 
    }
    
}
